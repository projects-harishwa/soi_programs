#include <stdio.h>

int main() {
    float num;
    printf("Enter a number: ");
    scanf("%f",&num);
    int int_num = (int)num; //this is really great sir!!
    if(int_num%2==0)
    {
        printf("%.5f is even",num);
    }
    else
    {
        printf("%.5f is odd",num);
    }
}
